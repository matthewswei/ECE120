; The purpose of this program is to print out a 8x16 bit image of an ASCII value stored at memory 
; location x5002 using two ASCII values stored at x5000 and x5001. This is done by reading font
; data that is given to us. I approached this problem by thinking what it would look like in java.
; This helped me realize how many loops and counters I needed. After that, I had to find a way to
; get the font data address for the ASCII in x5002 and a way to check if a bit is a 0 or 1. This
; part took the longest to complete and I did it by using FILL and labeling the location. After I
; figured that out, I just had to add some things here and there and test it for a working program.

; R0 stores the value that will be printed out for the output
; R1 stores the address of SUB, the value of which is used to AND it with FONT_DATA to determine
; the locations of the 0s and 1s
; R2 is used as an extra register to do operations which reqire the other registers to remain
; untouched
; R3 initially stores the ASCII value acts as a counter to determine which FONT_DATA 
; address needs to be used to for printing, but later on it is used to print 8 bits per line
; R4 stores the value of the current FONT_DATA that is being used to print
; R5 stores the address of the FONT_DATA that needs to be printed
; R6 stores the value that is ANDed with the FONT_DATA to determine where the 0s and 1s are
; R7 is the counter for how many lines will be printed for the output

		.ORIG x3000 ; This is the address where the program starts

		AND R0,R0,#0 ; Reset the value of R0 to 0
		AND R1,R1,#0 ; Reset the value of R1 to 0
		AND R2,R2,#0 ; Reset the value of R2 to 0
		AND R3,R3,#0 ; Reset the value of R3 to 0
		AND R4,R4,#0 ; Reset the value of R4 to 0
		AND R6,R6,#0 ; Reset the value of R6 to 0
		AND R7,R7,#0 ; Reset the value of R7 to 0
		ADD R7,R7,#15 ; Add 15 to line counter
		ADD R7,R7,#1 ; Add 1 to line counter
		LEA R5,FONT_DATA ; Load the address of FONT_DATA into FONT_DATA register
		LDI R3,CHAR ; Load the value in x5002 into the ASCII register
		BRz START ; Branch to START if the value is 0
COUNTER		ADD R4,R4,#15 ; Add 15 to address adder
		ADD R4,R4,#1 ; Add 1 to address adder
		ADD R3,R3,#-1 ; Subtract 1 from ASCII register counter
		BRp COUNTER ; Branch to counter if the value of R3 is positive
		ADD R5,R5,R4 ; Add offset to initial FONT_DATA location
START		LDR R4,R5,#0 ; Load R4 with the value that needs to be check with 0s and 1s
		AND R3,R3,#0 ; Reset the value of R3 to zero
		ADD R3,R3,#8 ; ADD 8 to R3 to use as a bit counter
		LD R6,SUB ; Load value of SUB to R6 for 0 and 1 checking
		LEA R1,SUB ; Load address of SUB to R1
CHECK		AND R2,R4,R6 ; AND R4 with R6 to check 1 and 0
		BRz ZERO ; Branch to ZERO if value is zero
		BRnp ONE ; Branch to ONE if value is negative or positive
ZERO		LDI R0,BACK ; Load the value from x5000 to R0 for printing
		OUT ; Print out R0
		BRnzp SKIP ; Branch to SKIP
ONE		LDI R0,FORWARD ; Load the value from x5000 to R0 for printing
		OUT ; Print out R0
		AND R0,R0,#0 ; Reset the value of R0 to 0
SKIP		ADD R3,R3,#-1 ; Subtract 1 from bit counter
		BRz NEXT ; Branch to next of value is 0
		ADD R1,R1,#1 ; Add address with 1 to get new address for SUB
		LDR R6,R1,#0 ; Load the value in memory location R1 into R6
		BRnzp CHECK ; Branch back to CHECK
NEXT		ADD R7,R7,#-1 ; Subtract 1 from line counter
		BRz STOP ; Branch to stop if the value is zero
		LD R0,SINGLE ; Load R0 with the value in SINGLE so that a new line can be made
		OUT ; Print out R0
		AND R0,R0,#0 ; Reset the value of R0 to 0
		ADD R5,R5,#1 ; Add 1 to the address counter for new address location
		BRp START ; Branch to START if positive
STOP		HALT ; Stop the program
		
SUB	.FILL x8000 ; Value to be ANDed to check for 0s and 1s
	.FILL x4000 ; Value to be ANDed to check for 0s and 1s
	.FILL x2000 ; Value to be ANDed to check for 0s and 1s
	.FILL x1000 ; Value to be ANDed to check for 0s and 1s
	.FILL x0800 ; Value to be ANDed to check for 0s and 1s
	.FILL x0400 ; Value to be ANDed to check for 0s and 1s
	.FILL x0200 ; Value to be ANDed to check for 0s and 1s
	.FILL x0100 ; Value to be ANDed to check for 0s and 1s
SINGLE  .FILL x000A ; Value if printed will skip a line
BACK    .FILL x5000 ; Address for the letter to be printed if bit is 0
FORWARD .FILL x5001 ; Address for the letter to be printed if bit is 1
CHAR    .FILL x5002 ; Address for the ASCII value
FONT_DATA ; Data for all the ASCII digets for printing
