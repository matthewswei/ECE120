		.ORIG x3000 ; This is the address where the program starts

BEGIN		LD R2,CHAR ; Load CHAR into R2
		AND R0,R0,#0 ; Reset the value of R0 to 0
		AND R1,R1,#0 ; Reset the value of R1 to 0
		AND R6,R6,#0 ; Reset the value of R6 to 0
NEW_CHAR	AND R3,R3,#0 ; Reset value of R3 to 0
		AND R4,R4,#0 ; Reset value of R4 to 0
		ADD R3,R2,#0 ; Add address location for character being printed to R3
		LDR R2,R2,#0 ; Load value of character into R2 for determining offset
		BRz LINE ; If value is nullstring, then branch to LINE
COUNTER		ADD R4,R4,#15 ; Add 15 to address adder
		ADD R4,R4,#1 ; Add 1 to address adder
		ADD R2,R2,#-1 ; Subtract 1 from ASCII register counter
		BRp COUNTER ; Branch to counter if the value of R3 is positive
		ADD R2,R3,#0 ; Set R2 equal to the address container
		LEA R5,FONT_DATA ; Load the address of FONT_DATA into FONT_DATA register
		ADD R5,R5,R4 ; Add offset to initial FONT_DATA location
		LD R4,OFFSET ; Load line offset to R4
		ADD R5,R5,R4 ; Add line offset to FONT_DATA offset for character
START		LDR R4,R5,#0 ; Load R4 with the value that needs to be check with 0s and 1s
		AND R3,R3,#0 ; Reset value of R3 to 0
		ADD R3,R3,#8 ; ADD 8 to R3 to use as a bit counter
		LD R6,SUB ; Load value of SUB to R6 for 0 and 1 checking
		LEA R1,SUB ; Load address of SUB to R1
CHECK		AND R6,R4,R6 ; AND R4 with R6 to check 1 and 0
		BRz ZERO ; Branch to ZERO if value is zero
		BRnp ONE ; Branch to ONE if value is negative or positive
ZERO		LDI R0,BACK ; Load the value from x5000 to R0 for printing
		OUT ; Print out R0
		BRnzp SKIP ; Branch to SKIP
ONE		LDI R0,FORWARD ; Load the value from x5000 to R0 for printing
		OUT ; Print out R0
		AND R0,R0,#0 ; Reset the value of R0 to 0
SKIP		ADD R3,R3,#-1 ; Subtract 1 from bit counter
		BRz NEW ; Branch to NEW
		ADD R1,R1,#1 ; Add address with 1 to get new address for SUB
		LDR R6,R1,#0 ; Load the value in memory location R1 into R6
		BRnzp CHECK ; Branch back to CHECK
NEW		ADD R2,R2,#1 ; Add 1 to get new address for the next ASCII
		BRnzp NEW_CHAR ; Branch back to NEW_CHAR
LINE		LD R5,LINE_C ; Load line counter value into R5
		ADD R5,R5,#-1 ; Subtract 1 from line coutner
		ST R5,LINE_C ; Store new line coutner value into LINE_C
		BRz STOP ; Branch to STOP
		LD R0,SINGLE ; Load R0 with the value in SINGLE so that a new line can be made
		OUT ; Print out R0
		AND R0,R0,#0 ; Reset the value of R0 to 0
		LD R5,OFFSET ; Load R5 with line offset
		ADD R5,R5,#1 ; Add 1 to the line offset
		ST R5,OFFSET ; Store the new line offset into offset
		BRnzp BEGIN ; Branch back to BEGIN 
STOP		HALT ; Stop the program
		
