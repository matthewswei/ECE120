; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.
; 
; The purpose of this program is to pring out a string stored in x5002 and beyond and ending when
; there is a null value. It prints the characters with the ASCII in x5001 and the empty spots with
; the ASCII in x5000. When I started writing the code, I copied and pasted my code from lab 12 over
; since they shared many similarities. Off the bat, I could tell I needed extra registers, so I
; simplified my code down from lab 12 because my lab 12 code used R7. After I fixed it, I thought
; about how I would write it in Java and added the necissary loops at said locations. I incrimented
; a way to keep track of where in the string the program was printing. I started off printing only
; one line and after that, I added an offset to determine how far in the lines the program had got
; so it could still find the correct ASCII value. After that, I just had to do some fine tuning as
; most of the big things have been done. The final thing I added was a way to detect if a NULL
; value had been detected. After that, I just had to debug some errors in the code and it resulted
; in a finish product.
;
;
; Register Table:
; R0 was used only to store the value that needed to be printed out.
; R1 was only used to store the address of the value that checked if a bit was a 1 or 0.
; R2 was both used as a counter that determined where in FONT_DATA the program should be reading as
; well as a storage for the current address of the ASCII value at x5002 and greater.
; R3 is used as the bit counter as well as a temporary register for R2.
; R4 is both used as a register that calculates and contains the offset to determine where in
; FONT_DATA the program needs to find the ASCII value's printing format. It also contains the value
; that needs to be checked where the 0s and 1s are.
; R5 is used to contain the address of where the program needs to start reading in FONT_DATA. Later
; in different parts, it is also used as the register that holds and calulates the line offset and
; line counter.
; R6 was only used to store the value of what checked if the bit was a 1 or 0.

